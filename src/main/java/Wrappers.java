public class Wrappers {
    public static void main(String[] args) {
        Byte a = new Byte(20);
        Short b = new Short(200);
        Integer c = new Integer(20000);
        Long d = new Long(2000000000L);
        Double e = new Double(200.22);
        Float f = new Float(20.2F);
        Boolean g = new Boolean(true);
        Character h = new Character('H');
    }
}
